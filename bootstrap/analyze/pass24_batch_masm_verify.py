# Batch driver
# --------------------------------------------------------------------------

def stage_asm_files(candidates):
    """Generate the per-candidate .ASM files. Returns a list of dicts with
    metadata ready for batch assembly. Filenames are kept to 8 chars max
    (MASM 4.0 8.3 limitation)."""
    out = []
    short_used = set()
    for c in candidates:
        unique = f"{c['module']}_{c['name']}"
        short = unique[:8].upper()
        base = short
        n = 0
        while base in short_used:
            n += 1
            # When N is 1..9 we have 7+1=8 chars; for n>=10 we have to drop
            # another char of the prefix so the total stays at 8.
            digits = str(n)
            base = (short[:8 - len(digits)] + digits)
        short_used.add(base)
        asm_path = WORK / f'{base}.ASM'
        byte_seq = bytes.fromhex(c['body_bytes_hex'])
        src = make_asm_source(c['name'], c['asm_lines'], byte_seq,
                                has_frame=c.get('has_frame', False))
        asm_path.write_text(src, encoding='ascii')
        out.append({**c, 'short': base, 'asm_path': asm_path,
                    'byte_seq': byte_seq})
    return out


def write_build_bat(asm_files):
    bat = ['echo === pass24 batch === > out.txt']
    for a in asm_files:
        bat.append(f"echo --- {a['short']} ({a['module']}::{a['name']}) --- >> out.txt")
        bat.append(f"masm {a['short']},{a['short']},{a['short']},nul; > {a['short']}.LOG")
        bat.append(f"type {a['short']}.LOG >> out.txt")
    bat.append('echo === DONE === >> out.txt')
    (WORK / 'build.bat').write_text('\n'.join(bat) + '\n', encoding='ascii')


def run_masm_batch():
    dosbuild = REPO / 'tools' / 'dos' / 'dosbuild.sh'
    cmd = ['wsl', '--', 'bash', str(dosbuild).replace('\\', '/').replace(
        'C:', '/mnt/c'), str(WORK).replace('\\', '/').replace('C:', '/mnt/c'),
        'build.bat']
    r = subprocess.run(cmd, capture_output=True, text=True, timeout=300)
    return r


def inspect_round(asm_files):
    """For each file, check whether its OBJ contains the expected bytes.
    Returns a list of dicts."""
    results = []
    for a in asm_files:
        obj = WORK / f"{a['short']}.OBJ"
        lst = WORK / f"{a['short']}.LST"
        expected = a['byte_seq']
        actual, note = find_func_bytes_in_obj(obj, expected)
        match = (actual == expected)
        results.append({
            **{k: a[k] for k in ('module', 'name', 'short',
                                   'instruction_count')},
            'expected_hex': expected.hex().upper(),
            'actual_hex': actual.hex().upper() if actual else '',
            'match': match,
            'note': note,
            'lst_path': str(lst),
            'asm_path': str(a['asm_path']),
        })
    return results


def refine_candidates(asm_files, results):
    """For every non-matching candidate that produced an OBJ + LST, REBUILD
    its .asm body from the .LST itself - keeping each instruction's source
    text when it assembled correctly, and replacing it with a `db` directive
    when MASM picked a different encoding. Returns the list of files that
    were actually refined."""
    refined = []
    for a, r in zip(asm_files, results):
        if r['match']:
            continue
        lst = Path(r['lst_path'])
        if not lst.exists():
            continue
        records = parse_lst_instructions(lst, a['name'])
        if not records:
            continue
        new_body, n_changed = rebuild_body_from_lst(records, a['byte_seq'])
        if new_body is None or n_changed == 0:
            continue
        # Rewrite the .asm with the new body
        current = a['asm_path'].read_text(encoding='ascii').splitlines()
        proc_start, proc_end = None, None
        for i, ln in enumerate(current):
            if re.search(rf"\b{re.escape(a['name'])}\s+PROC\s+FAR", ln):
                proc_start = i + 1
            elif proc_start is not None and re.search(
                    rf"\b{re.escape(a['name'])}\s+ENDP", ln):
                proc_end = i
                break
        if proc_start is None or proc_end is None:
            continue
        new_full = current[:proc_start] + new_body + current[proc_end:]
        a['asm_path'].write_text('\n'.join(new_full) + '\n', encoding='ascii')
        refined.append((a['short'], n_changed))
    return refined


