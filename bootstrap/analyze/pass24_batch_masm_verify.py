_SEG_PREFIX_BYTES = {0x26, 0x2E, 0x36, 0x3E, 0x64, 0x65}
_PREFIX_BYTES = _SEG_PREFIX_BYTES | {0x66, 0x67, 0xF0, 0xF2, 0xF3}


def _shorter_encoding_exists(ins_bytes):
    """Heuristic: would MASM 4.0 emit fewer bytes for this instruction?

    Returns True if the instruction uses a non-minimal ModRM displacement
    encoding (mod=10 with a disp16 that fits in disp8 or is 0; mod=01 with
    disp8==0 for non-[bp] forms). Those are the cases where MASM picks the
    short form even though the original binary kept the long one."""
    i = 0
    n = len(ins_bytes)
    while i < n and ins_bytes[i] in _PREFIX_BYTES:
        i += 1
    if i + 1 >= n:
        return False
    # Skip 2-byte opcodes (0x0F xx)
    if ins_bytes[i] == 0x0F:
        i += 1
        if i + 1 >= n:
            return False
    modrm_pos = i + 1
    if modrm_pos >= n:
        return False
    modrm = ins_bytes[modrm_pos]
    mod = (modrm >> 6) & 0b11
    rm = modrm & 0b111
    # 16-bit addressing: no SIB byte. Direct addressing form is mod=00 rm=110
    # which uses disp16 (a "[disp16]" address), not eligible for shortening.
    if mod == 0b10:
        # disp16 follows ModRM
        disp_pos = modrm_pos + 1
        if disp_pos + 2 > n:
            return False
        disp = int.from_bytes(ins_bytes[disp_pos:disp_pos + 2],
                               'little', signed=True)
        if -128 <= disp <= 127:
            return True
    elif mod == 0b01:
        # disp8 follows ModRM. mod=01 with disp8=0 is only mandatory for
        # rm=110 ([bp]); for any other r/m the same effective address can
        # be encoded with mod=00 (no disp).
        disp_pos = modrm_pos + 1
        if disp_pos >= n:
            return False
        disp = ins_bytes[disp_pos]
        if disp == 0 and rm != 0b110:
            return True
    return False


def patch_ambiguous_xchg(asm_lines, byte_seq):
    """Emit raw db bytes for instructions whose Capstone disasm wouldn't
    re-assemble back to the same bytes via MASM 4.0.

    Three classes of ambiguity are handled here:

    1. `xchg <reg16>, <reg16>` (excluding ax): `87 r/m,r` and `87 r,r/m`
       both encode the swap; MASM picks one, the original may use the other.

    2. Any instruction whose first byte is a segment-override prefix
       (2E / 26 / 36 / 3E / 64 / 65) when Capstone's mnemonic is one MASM
       can't encode with that prefix (xlat, string ops without indirect
       form, etc.). We emit the prefix + opcode verbatim.

    3. Any instruction using a non-minimal ModRM displacement encoding
       (mod=10 with disp16 fitting in disp8 / disp16==0; mod=01 with
       disp8==0 for non-[bp] forms). MASM 4.0 always picks the shortest
       encoding, so when the original binary used the longer form we have
       to bypass MASM.
    """
    import capstone
    md = capstone.Cs(capstone.CS_ARCH_X86, capstone.CS_MODE_16)
    md.detail = False
    insns = list(md.disasm(byte_seq, 0))
    if len(insns) != len(asm_lines):
        return asm_lines
    # xchg with ax has the unique 1-byte 90+r encoding (1 byte, not 2),
    # so ax is excluded from xchg. test has no 1-byte form -> ax is fine.
    gpr16_no_ax = {'cx', 'dx', 'bx', 'sp', 'bp', 'si', 'di'}
    gpr8 = {'al', 'cl', 'dl', 'bl', 'ah', 'ch', 'dh', 'bh'}
    xchg_regs = gpr16_no_ax | gpr8
    test_regs = xchg_regs | {'ax'}
    out = []
    for line, ins in zip(asm_lines, insns):
        ib = bytes(ins.bytes)
        replaced = False
        # 1) Symmetric reg-reg ambiguity for opcodes that don't have a
        #    "preferred direction" in the encoding. ModRM can place either
        #    operand in the reg field, MASM picks one, original may differ.
        #
        #    - xchg reg, reg  (excluding ax which has the 90+r short form)
        #    - test reg, reg  (single opcode 85 / 84, fully commutative;
        #      ax is OK here, test has no 1-byte form)
        if (not replaced and ins.size == 2 and
                ins.mnemonic in ('xchg', 'test')):
            ops = [o.strip() for o in ins.op_str.split(',')]
            allowed = test_regs if ins.mnemonic == 'test' else xchg_regs
            if (len(ops) == 2 and ops[0] in allowed and ops[1] in allowed):
                line = db_directive(
                    ib,
                    f'was {ins.mnemonic} {ops[0]}, {ops[1]} (enc ambiguity)')
                replaced = True
        # 2) Instruction with a leading segment-override prefix whose final
        #    asm line (after rewrite_for_masm stripped operands etc.) no
        #    longer carries the `seg:` annotation. Without it MASM emits
        #    the bare opcode and the override byte is lost. Patching this
        #    based on the post-rewrite `line` is the right place: the
        #    `rewrite_for_masm` for string ops drops the entire operand
        #    list, so its output never has `es:` even when Capstone's
        #    original op_str did.
        if (not replaced and ib and ib[0] in _SEG_PREFIX_BYTES):
            # MASM 4.0 silently strips REDUNDANT segment overrides. A
            # CS:/SS:/ES:/DS: prefix that doesn't change the effective
            # segment (e.g. `ds:` on `[bx+si]`, which is already DS) is
            # dropped. FS:/GS: aren't understood at all. The safest
            # behaviour is to db on ANY seg-override prefix, regardless
            # of whether the source line still carries the `seg:` text.
            line = db_directive(
                ib, f'was {ins.mnemonic} with seg override prefix')
            replaced = True
        # 2b) F2/F3 prefix (REP/REPNE/LOCK-like) on an instruction that
        #     ISN'T a string op. Capstone emits the bare mnemonic without
        #     a `rep`/`repne`/`repe` keyword in the source line, and MASM
        #     happily strips the redundant prefix byte. Detect by
        #     checking the leading byte is F2/F3 and the mnemonic isn't
        #     a recognised string op.
        if not replaced and ib and ib[0] in (0xF2, 0xF3):
            string_ops = {'movsb', 'movsw', 'movsd', 'cmpsb', 'cmpsw',
                            'scasb', 'scasw', 'stosb', 'stosw', 'lodsb',
                            'lodsw', 'insb', 'insw', 'outsb', 'outsw'}
            line_lower = line.lower()
            has_rep = any(s in line_lower for s in
                           ('rep ', 'repe ', 'repne ', 'repz ', 'repnz ',
                            'lock '))
            if ins.mnemonic not in string_ops and not has_rep:
                line = db_directive(
                    ib, f'was {ins.mnemonic} with f2/f3 prefix')
                replaced = True
        # 3) Instruction Capstone disassembled using 32-bit registers
        #    (eax/ebx/ecx/edx/esp/ebp/esi/edi). This happens when there's
        #    an address-size override prefix (0x67) in 16-bit code, often
        #    a sign that those bytes are actually inlined data
        #    (jump tables, vtables, etc.) being misinterpreted as code.
        #    MASM 4.0 doesn't know 32-bit registers, so we have to emit
        #    raw bytes.
        if (not replaced and ins.op_str and
                re.search(r'\be[abcd]x\b|\be[bs]p\b|\be[sd]i\b',
                          ins.op_str)):
            line = db_directive(
                ib, f'was {ins.mnemonic} {ins.op_str} (32-bit reg in 16-bit code)')
            replaced = True
        # 4) Operand-size prefix (0x66) leading byte in 16-bit code. This
        #    is a 386+ prefix to switch operand size; MASM 4.0 strips
        #    redundant 66 prefixes silently, and the 32-bit forms are
        #    usually a sign of inlined string/data being misread as code.
        if not replaced and ib and ib[0] == 0x66:
            line = db_directive(
                ib, f'was {ins.mnemonic} {ins.op_str} (operand-size prefix)')
            replaced = True
        # 5) Address-size prefix (0x67) leading byte: we already catch
        #    most via the 32-bit reg check, but some encodings collapse
        #    back to 16-bit syntax that MASM happily compiles to a
        #    different (shorter) form. Be conservative: any 67 prefix
        #    is db'd.
        if not replaced and ib and ib[0] == 0x67:
            line = db_directive(
                ib, f'was {ins.mnemonic} {ins.op_str} (address-size prefix)')
            replaced = True
        # 6) Floating-point instructions. Capstone happily disassembles
        #    f-prefixed FPU mnemonics (fadd, fnsave, fimul, fninit,
        #    fnstcw, etc.) but MASM 4.0 needs `.8087` enabled and even
        #    then its encoding choices for memory operands frequently
        #    differ from what the original binary used. The original
        #    bytes are unambiguous, so emit them verbatim.
        if not replaced and ins.mnemonic.startswith('f'):
            # Skip FAR/FAR-jump style fakes (e.g. far jmp 'jmp far')
            # by checking we actually had a multi-byte FPU encoding.
            # Real FPU first byte is in D8h..DFh.
            if ib and 0xD8 <= ib[0] <= 0xDF:
                line = db_directive(
                    ib, f'was {ins.mnemonic} {ins.op_str} (FPU - MASM 4.0 reenc)')
                replaced = True
        # 6b) Instructions MASM 4.0 doesn't know:
        #     - int3 (CC) - the canonical mnemonic is `int 3` (CD 03);
        #       Capstone uses the short form. We pick the original byte.
        #     - pushaw (60) / popaw (61) - 286+ instructions.
        #     - ud0 (0F FF) - undefined-instruction trap (Pentium MMX+),
        #       in 16-bit code this is just data.
        #     - arpl (63) - 286 protected-mode-only; rarely a real op.
        #     - bound (62) - 186/286 array index check.
        #     - enter (C8) / leave (C9) - 186+; .186 covers them but
        #       Capstone's encoding may differ on edge cases.
        #     - insb/outsb/insw/outsw (6C-6F) - 186+ port I/O strings.
        unknown_mnemonics = {
            'int3', 'ud0', 'ud1', 'ud2',
            'pushaw', 'popaw', 'pusha', 'popa',
            'arpl', 'bound',
            # cmov* are P6+ (Pentium Pro 1995). Capstone happily emits
            # them, MASM 4.0 has no idea.
            'cmove', 'cmovne', 'cmovz', 'cmovnz',
            'cmovs', 'cmovns', 'cmovo', 'cmovno',
            'cmovp', 'cmovnp', 'cmovpe', 'cmovpo',
            'cmova', 'cmovae', 'cmovb', 'cmovbe',
            'cmovg', 'cmovge', 'cmovl', 'cmovle',
            'cmovc', 'cmovnc',
        }
        if not replaced and ins.mnemonic in unknown_mnemonics:
            line = db_directive(
                ib, f'was {ins.mnemonic} {ins.op_str} (286+/unknown to MASM 4.0)')
            replaced = True
        # 6b-2) Any instruction whose first byte is 0x0F (the two-byte
        #     opcode escape). 0F-prefix instructions are 286+ at minimum
        #     (LSL, LAR, SLDT, etc.) and MMX/SSE at the higher end
        #     (PACKSSWB, MOVD, etc.). MASM 4.0's coverage is patchy and
        #     in any case the operand syntax for these often doesn't
        #     round-trip. The original bytes are unambiguous, so emit
        #     them verbatim.
        if not replaced and ib and ib[0] == 0x0F:
            line = db_directive(
                ib, f'was {ins.mnemonic} {ins.op_str} (0F-escape opcode)')
            replaced = True
        # 6b-3) `imul reg, r/m, imm` with a 16-bit immediate that fits
        #     in a signed 8-bit value. The 0x69 opcode takes a 16-bit
        #     immediate; the 0x6B opcode takes a sign-extended 8-bit
        #     immediate. MASM 4.0 always picks the shorter 0x6B form
        #     when the imm fits, but the original binary may use the
        #     longer 0x69 form for fixed instruction-size reasons.
        if (not replaced and ib and ib[0] == 0x69 and len(ib) >= 4):
            # ib = 69 modrm [disp16?] imm16 - imm is the last 2 bytes.
            imm = int.from_bytes(ib[-2:], 'little', signed=True)
            if -128 <= imm <= 127:
                line = db_directive(
                    ib, f'was {ins.mnemonic} {ins.op_str} (imul imm fits in 8-bit)')
                replaced = True
        # 6c) `ptr [...]` standalone (no size keyword) in Capstone's output.
        #     This happens for `les`/`lds` instructions and similar, where
        #     Capstone prints `les bx, ptr [bp + 0x10]`. MASM parses
        #     `ptr` as a symbol name there, leading to wrong encoding.
        #     The size for les/lds is always dword.
        if not replaced and ', ptr [' in line.lower():
            line = re.sub(r',\s*ptr\s*\[', ', dword ptr [', line,
                          flags=re.IGNORECASE)
        # 7) non-minimal ModRM displacement (disp16 fits in disp8, etc.)
        if not replaced and _shorter_encoding_exists(ib):
            line = db_directive(
                ib, f'was {ins.mnemonic} (non-minimal ModRM enc)')
            replaced = True
        out.append(line)
    return out


def make_asm_source(name, asm_lines, byte_seq, has_frame=False):
    """Wrap a function body in a minimal MASM source unit.

    If `has_frame` is True we add a near-dummy DGROUP/DATA segment so the
    cMacros prologue references work; for now we just emit the same skeleton.
    """
    cleaned = []
    for ln in asm_lines:
        c = clean_asm_line(ln)
        c = rewrite_for_masm(c)
        if c:
            cleaned.append(c)
    cleaned = patch_dangling_labels(cleaned, byte_seq)
    cleaned = patch_ambiguous_xchg(cleaned, byte_seq)
    body = '\n'.join('        ' + ln.lstrip() if not ln.lstrip().startswith('db ')
                    else ln for ln in cleaned)
    return f"""\
; Auto-generated for byte-exact verification (pass24).
; Original function: {name}

        .186                     ; enable 80186/80286 immediate-shift opcodes

_TEXT   SEGMENT BYTE PUBLIC 'CODE'
        ASSUME  CS:_TEXT

PUBLIC  {name}

{name} PROC FAR
{body}
{name} ENDP

_TEXT   ENDS
        END
"""


# --------------------------------------------------------------------------
# .LST parsing
# --------------------------------------------------------------------------

LST_INSTR_RE = re.compile(
    r'^\s*([0-9A-Fa-f]{4})\s+'                       # offset
    # 1+ hex tokens, separated by EITHER ":" (with optional single space),
    # exactly one space, OR "/ " which MASM uses to mark a REP/REPNE/LOCK
    # prefix (e.g. "F3/ A5  rep movsw"). The grouping is tight enough to
    # prevent greedy matching from eating instruction mnemonics that
    # happen to be valid hex words (e.g. `dec`, `add`, `cbf`, etc.) which
    # sit after the wider source-column gap.
    r'([0-9A-Fa-f]{2,8}(?:(?:/ |:[ ]?| )[0-9A-Fa-f]{2,8})*)'
    r'\s{2,}'                                         # wide gap (>=2 spaces)
    r'([A-Za-z][^\r\n]*?)$'                           # source (starts with letter)
)


def _lst_token_to_bytes(token):
    """Convert a single LST hex token to actual on-disk bytes.

    MASM 4.0 displays:
      - 8-bit immediates and bytes as 2 hex digits (e.g. `8B`)        -> 1 byte
      - 16-bit immediates / operands as 4 hex digits (e.g. `0002`)     -> 2 bytes LE
      - 32-bit immediates as 8 hex digits (rare)                        -> 4 bytes LE
    """
    n = len(token)
    val = int(token, 16)
    if n == 2:
        return bytes([val])
    if n == 4:
        return bytes([val & 0xff, (val >> 8) & 0xff])
    if n == 8:
        return bytes([val & 0xff, (val >> 8) & 0xff,
                       (val >> 16) & 0xff, (val >> 24) & 0xff])
    # Fallback: parse as raw hex
    if n % 2 == 0:
        return bytes.fromhex(token)
    return b''


def parse_lst_instructions(lst_path, func_name):
    """Parse a MASM 4.0 listing and return a list of dicts:
        { 'offset': int, 'bytes': bytes, 'source': str }
    for every instruction inside the named PROC FAR ... ENDP block.
    """
    if not lst_path.exists():
        return []
    text = lst_path.read_text(encoding='ascii', errors='ignore')
    out = []
    in_proc = False
    proc_start_offset = None
    for line in text.splitlines():
        if re.search(rf'\b{re.escape(func_name)}\s+PROC\s+FAR\b', line):
            in_proc = True
            continue
        if re.search(rf'\b{re.escape(func_name)}\s+ENDP\b', line):
            break
        if not in_proc:
            continue
        m = LST_INSTR_RE.match(line)
        if not m:
            continue
        offset = int(m.group(1), 16)
        if proc_start_offset is None:
            proc_start_offset = offset
        byte_str = m.group(2).replace(':', ' ').replace('/', ' ')
        byte_tokens = byte_str.split()
        try:
            raw = b''.join(_lst_token_to_bytes(t) for t in byte_tokens)
        except ValueError:
            continue
        out.append({
            'offset': offset - proc_start_offset,
            'bytes': raw,
            'source': m.group(3).strip(),
        })
    return out


# --------------------------------------------------------------------------
# Refinement: replace diverging instructions with `db` raw bytes
# --------------------------------------------------------------------------

def rebuild_body_from_lst(lst_records, expected_bytes):
    """Rebuild the function body using the .LST records as the source of
    truth. For each instruction whose generated bytes match expected at the
    same offset, emit its original source text. For each mismatch, emit a
    `db` directive carrying the ORIGINAL bytes.

    Returns (new_body_lines, n_refinements) or (None, 0) if the .LST records
    don't cover the whole expected byte range (in which case we can't safely
    rebuild without risking truncation).
    """
    if not lst_records:
        return None, 0

    # Verify .LST bytes cover the full expected range exactly.
    total_generated = sum(len(r['bytes']) for r in lst_records)
    if total_generated != len(expected_bytes):
        # The .LST didn't produce bytes 1:1 with the original (length diff).
        # We can't safely rebuild because the instruction lengths differ.
        return None, 0

    out = []
    n_changed = 0
    for rec in lst_records:
        off = rec['offset']
        gen = rec['bytes']
        expected_chunk = expected_bytes[off:off + len(gen)]
        if gen == expected_chunk:
            # Keep the original source line. It assembled correctly.
            # Note: `source` from .LST has trimmed whitespace; preserve a
            # standard indent.
            src = rec['source']
            out.append('        ' + src)
        else:
            n_changed += 1
            out.append(db_directive(
                expected_chunk,
                f"was '{rec['source']}' (MASM encoding differed)"))
    return out, n_changed


