"""Pass 24: Batch byte-exact verification with iterative refinement.

For each mini-stub candidate from pass23:
  1. Generate a .asm with the disassembled instructions (capstone-style)
     after applying a long list of pretty-printer -> MASM 4.0 fixups.
  2. Assemble with MASM 4.0 inside DOSBox-X (one spawn for the whole batch).
  3. Read the produced .OBJ, locate the function's bytes inside, compare
     against the original bytes.
  4. ITERATIVE REFINEMENT: for any function whose generated bytes diverge
     from the original (encoding ambiguities like `xchg ch, cl` 86 CD vs
     86 E9), parse the .LST file to identify the offending instruction(s)
     and replace them with raw `db` directives carrying the ORIGINAL
     bytes. Re-assemble and re-check. This iterates up to N rounds or
     until no further progress is made.

The end goal: every recoverable function ends up as a .asm that MASM 4.0
compiles to byte-exact original output, possibly with some instructions
emitted as `db` sequences when MASM's preferred encoding differs.

Artifacts:
  tools/dos/work/batch/<MOD>_<NAME>.asm  - per-function source (final form)
  tools/dos/work/batch/<MOD>_<NAME>.obj  - assembled by MASM
  tools/dos/work/batch/<MOD>_<NAME>.lst  - assembler listing (offsets+bytes)
  state/analyze/pass24/results.json      - per-function status, every round
  state/analyze/pass24/summary.md        - human-readable report
"""
import json
import re
import shutil
import subprocess
from pathlib import Path

REPO = Path(__file__).resolve().parents[2]
PASS23 = REPO / 'state' / 'analyze' / 'pass23'
PASS24 = REPO / 'state' / 'analyze' / 'pass24'
WORK = REPO / 'tools' / 'dos' / 'work' / 'batch'

MAX_REFINEMENT_ROUNDS = 3


# --------------------------------------------------------------------------
# .asm pretty-printer cleanup
# --------------------------------------------------------------------------

def clean_asm_line(line):
    out = re.sub(r';.*$', '', line).rstrip()
    return out.lstrip()


def rewrite_for_masm(line):
    """Convert capstone-style disasm into MASM 4.0-friendly syntax."""
    def hex_fix(m):
        v = m.group(1)
        if v.lower().endswith('h'):
            return v
        if v.lower().startswith('0x'):
            v = v[2:]
            if v and v[0].isalpha():
                v = '0' + v
            return v + 'h'
        return v
    line = re.sub(r'\b(0x[0-9a-fA-F]+)\b', hex_fix, line)
    # 'retf' -> 'ret' (MASM auto-picks CB/CA based on PROC FAR)
    line = re.sub(r'\bretf\b', 'ret', line)
    # 'ljmp' / 'lcall' -> 'jmp dword ptr' / 'call dword ptr'
    line = re.sub(r'\bljmp\b', 'jmp dword ptr', line)
    line = re.sub(r'\blcall\b', 'call dword ptr', line)
    # 'mov sreg, [imm]' -> 'mov sreg, ds:[imm]'
    line = re.sub(
        r'(\bmov\s+(?:[ecds]s)\s*,\s*)(?:word\s+ptr\s+)?(\[)',
        r'\1ds:\2', line, flags=re.I)
    # 'les di, ptr ss:[bx+4]' -> 'les di, ss:[bx+4]'
    line = re.sub(r'(\b(?:les|lds|lfs|lgs|lss)\s+\w+\s*,\s*)ptr\s+',
                   r'\1', line, flags=re.I)
    # String instructions: drop operand list -- MASM 4.0 wants implicit form
    line = re.sub(
        r'\b(rep|repe|repz|repne|repnz)?\s*'
        r'(scasb|scasw|movsb|movsw|stosb|stosw|lodsb|lodsw|cmpsb|cmpsw|insb|insw|outsb|outsw)\b'
        r'.*$',
        lambda m: f"{m.group(1) or ''} {m.group(2)}".strip(),
        line, flags=re.I)
    # Capstone in 16-bit mode mislabels 99h -> 'cdq'; in 16-bit it's 'cwd'
    line = re.sub(r'\bcdq\b', 'cwd', line, flags=re.I)
    line = re.sub(r'\bcwde\b', 'cbw', line, flags=re.I)
    # 'pop word ptr cs:[X]' -> 'pop cs:[X]'
    line = re.sub(r'(\bpop\s+)word\s+ptr\s+', r'\1', line, flags=re.I)
    # 'push word ptr cs:[X]' -> 'push cs:[X]'
    line = re.sub(r'(\bpush\s+)word\s+ptr\s+(?=[ecds]s:)',
                   r'\1', line, flags=re.I)
    # Capstone sometimes writes 'jmpf' for far jumps; MASM wants 'jmp far ptr'
    line = re.sub(r'\bjmpf\b', 'jmp far ptr', line, flags=re.I)
    line = re.sub(r'\bcallf\b', 'call far ptr', line, flags=re.I)
    # Force memory operand parsing for bracketed absolute addresses.
    # `cmp al, byte ptr [04h]` gets parsed by MASM 4.0 as `cmp al, 4`
    # (immediate, because `[X]` with an integer is treated as a parenthesised
    # expression). The cure is an explicit segment override: `ds:[04h]`.
    # Apply only when there is no segment override already.
    line = re.sub(
        r'(?<![a-zA-Z][a-zA-Z]:)\[([0-9][0-9A-Fa-fxXhH]*)\]',
        r'ds:[\1]', line)
    return line


# --------------------------------------------------------------------------
# Dangling label patching (jumps/calls to L_xxxx not in our segment)
# --------------------------------------------------------------------------

def hex_lit(byte_v):
    s = f'{byte_v:02X}'
    return ('0' + s if s[0] in 'ABCDEF' else s) + 'h'


def db_directive(raw_bytes, comment=''):
    args = ', '.join(hex_lit(b) for b in raw_bytes)
    return f"        db    {args}    ; {comment}".rstrip()


def patch_dangling_labels(asm_lines, byte_seq):
    """Replace `j* L_xxxx` / `call L_xxxx` references with raw `db` bytes,
    matched in instruction order via a Capstone disassembly of byte_seq."""
    import capstone
    md = capstone.Cs(capstone.CS_ARCH_X86, capstone.CS_MODE_16)
    md.detail = False

    branch_bytes = []
    for ins in md.disasm(byte_seq, 0):
        if ins.mnemonic.startswith('j') or ins.mnemonic in (
                'call', 'loop', 'loope', 'loopne', 'loopz', 'loopnz'):
            op_str = ins.op_str.strip()
            if op_str.startswith('0x') or op_str.startswith('-'):
                branch_bytes.append(bytes(ins.bytes))

    out = []
    bidx = 0
    # A "dangling branch" line is any j*/jmp/call/loop whose target is either:
    #   - a label name (L_xxxx) we don't define, or
    #   - a numeric literal (e.g. `call 0`, `jmp 1234h`) that points outside
    #     our synthetic single-function segment.
    # In all such cases we replace the source with a raw db opcode sequence.
    branch_re = re.compile(
        r'^\s*(j\w+|jmp|call|loop\w*)\s+'
        r'(?:L_[0-9A-Fa-f]+|[-+]?[0-9][0-9A-Fa-fxXhH]*)'
        r'\s*$', re.I)
    for line in asm_lines:
        m = branch_re.match(line)
        if m and bidx < len(branch_bytes):
            raw = branch_bytes[bidx]
            mnem = m.group(1).lower()
            line = db_directive(raw, f"was {mnem} (raw bytes)")
            bidx += 1
        out.append(line)
    return out


# --------------------------------------------------------------------------
# Function source assembly
# --------------------------------------------------------------------------

def make_asm_source(name, asm_lines, byte_seq, has_frame=False):
    """Wrap a function body in a minimal MASM source unit.

    If `has_frame` is True we add a near-dummy DGROUP/DATA segment so the
    cMacros prologue references work; for now we just emit the same skeleton.
    """
    cleaned = []
    for ln in asm_lines:
        c = clean_asm_line(ln)
        c = rewrite_for_masm(c)
        if c:
            cleaned.append(c)
    cleaned = patch_dangling_labels(cleaned, byte_seq)
    body = '\n'.join('        ' + ln.lstrip() if not ln.lstrip().startswith('db ')
                    else ln for ln in cleaned)
    return f"""\
; Auto-generated for byte-exact verification (pass24).
; Original function: {name}

        .186                     ; enable 80186/80286 immediate-shift opcodes

_TEXT   SEGMENT BYTE PUBLIC 'CODE'
        ASSUME  CS:_TEXT

PUBLIC  {name}

{name} PROC FAR
{body}
{name} ENDP

_TEXT   ENDS
        END
"""


# --------------------------------------------------------------------------
# .LST parsing
# --------------------------------------------------------------------------

LST_INSTR_RE = re.compile(
    r'^\s*([0-9A-Fa-f]{4})\s+'                       # offset
    # 1+ hex tokens, separated by EITHER ":" (with optional single space) or
    # exactly one space. This is tight enough to prevent greedy matching from
    # eating instruction mnemonics that happen to be valid hex words (e.g.
    # `dec`, `add`, `cbf`, etc.) which sit after the wider source-column gap.
    r'([0-9A-Fa-f]{2,8}(?:(?::[ ]?| )[0-9A-Fa-f]{2,8})*)'
    r'\s{2,}'                                         # wide gap (>=2 spaces)
    r'([A-Za-z][^\r\n]*?)$'                           # source (starts with letter)
)


def _lst_token_to_bytes(token):
    """Convert a single LST hex token to actual on-disk bytes.

    MASM 4.0 displays:
      - 8-bit immediates and bytes as 2 hex digits (e.g. `8B`)        -> 1 byte
      - 16-bit immediates / operands as 4 hex digits (e.g. `0002`)     -> 2 bytes LE
      - 32-bit immediates as 8 hex digits (rare)                        -> 4 bytes LE
    """
    n = len(token)
    val = int(token, 16)
    if n == 2:
        return bytes([val])
    if n == 4:
        return bytes([val & 0xff, (val >> 8) & 0xff])
    if n == 8:
        return bytes([val & 0xff, (val >> 8) & 0xff,
                       (val >> 16) & 0xff, (val >> 24) & 0xff])
    # Fallback: parse as raw hex
    if n % 2 == 0:
        return bytes.fromhex(token)
    return b''


def parse_lst_instructions(lst_path, func_name):
    """Parse a MASM 4.0 listing and return a list of dicts:
        { 'offset': int, 'bytes': bytes, 'source': str }
    for every instruction inside the named PROC FAR ... ENDP block.
    """
    if not lst_path.exists():
        return []
    text = lst_path.read_text(encoding='ascii', errors='ignore')
    out = []
    in_proc = False
    proc_start_offset = None
    for line in text.splitlines():
        if re.search(rf'\b{re.escape(func_name)}\s+PROC\s+FAR\b', line):
            in_proc = True
            continue
        if re.search(rf'\b{re.escape(func_name)}\s+ENDP\b', line):
            break
        if not in_proc:
            continue
        m = LST_INSTR_RE.match(line)
        if not m:
            continue
        offset = int(m.group(1), 16)
        if proc_start_offset is None:
            proc_start_offset = offset
        byte_str = m.group(2).replace(':', ' ')
        byte_tokens = byte_str.split()
        try:
            raw = b''.join(_lst_token_to_bytes(t) for t in byte_tokens)
        except ValueError:
            continue
        out.append({
            'offset': offset - proc_start_offset,
            'bytes': raw,
            'source': m.group(3).strip(),
        })
    return out


# --------------------------------------------------------------------------
# Refinement: replace diverging instructions with `db` raw bytes
# --------------------------------------------------------------------------

def rebuild_body_from_lst(lst_records, expected_bytes):
    """Rebuild the function body using the .LST records as the source of
    truth. For each instruction whose generated bytes match expected at the
    same offset, emit its original source text. For each mismatch, emit a
    `db` directive carrying the ORIGINAL bytes.

    Returns (new_body_lines, n_refinements) or (None, 0) if the .LST records
    don't cover the whole expected byte range (in which case we can't safely
    rebuild without risking truncation).
    """
    if not lst_records:
        return None, 0

    # Verify .LST bytes cover the full expected range exactly.
    total_generated = sum(len(r['bytes']) for r in lst_records)
    if total_generated != len(expected_bytes):
        # The .LST didn't produce bytes 1:1 with the original (length diff).
        # We can't safely rebuild because the instruction lengths differ.
        return None, 0

    out = []
    n_changed = 0
    for rec in lst_records:
        off = rec['offset']
        gen = rec['bytes']
        expected_chunk = expected_bytes[off:off + len(gen)]
        if gen == expected_chunk:
            # Keep the original source line. It assembled correctly.
            # Note: `source` from .LST has trimmed whitespace; preserve a
            # standard indent.
            src = rec['source']
            out.append('        ' + src)
        else:
            n_changed += 1
            out.append(db_directive(
                expected_chunk,
                f"was '{rec['source']}' (MASM encoding differed)"))
    return out, n_changed


# --------------------------------------------------------------------------
# OBJ inspection
# --------------------------------------------------------------------------

def find_func_bytes_in_obj(obj_path, expected_bytes):
    if not obj_path.exists():
        return None, 'no obj produced'
    data = obj_path.read_bytes()
    idx = data.find(expected_bytes)
    if idx >= 0:
        return data[idx:idx + len(expected_bytes)], f'found at OBJ offset 0x{idx:x}'
    for prefix_len in range(len(expected_bytes) - 1, 0, -1):
        idx = data.find(expected_bytes[:prefix_len])
        if idx >= 0:
            actual = data[idx:idx + len(expected_bytes)]
            return actual, (f'partial match: first {prefix_len} bytes ok at '
                            f'OBJ offset 0x{idx:x}, then divergence')
    return None, 'no recognizable prefix in OBJ'


