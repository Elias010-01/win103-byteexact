def main():
    PASS24.mkdir(parents=True, exist_ok=True)
    WORK.mkdir(parents=True, exist_ok=True)
    CACHE.mkdir(parents=True, exist_ok=True)

    # Clean previous work artifacts (per-function files) and any leftover
    # worker subdirs. KEEP the cache: it's the whole point of the speedup.
    # File deletion can race with antivirus / open handles, so retry a
    # few times before giving up.
    import time
    for f in WORK.iterdir():
        if f.is_file():
            for attempt in range(5):
                try:
                    f.unlink()
                    break
                except PermissionError:
                    time.sleep(0.5)
            else:
                # As a last resort, leave it - we'll overwrite it later.
                pass
        elif f.is_dir() and f.name.startswith('worker_'):
            shutil.rmtree(f, ignore_errors=True)

    # Load ALL candidates. For each one we use `true_bytes_hex` from pass23
    # (which is capstone-disassembled forward from the function's entry in
    # the ORIGINAL binary, up to the first retf). That gives us a
    # length-accurate, byte-exact target. We then re-disassemble those
    # true bytes ourselves to produce the .asm source - bypassing pass1
    # entirely and removing any disagreement between pass1's .asm and the
    # original binary.
    import capstone
    md_global = capstone.Cs(capstone.CS_ARCH_X86, capstone.CS_MODE_16)
    md_global.detail = False

    # Load both pass23 (PROC FAR blocks from reconstructed .asm) and pass25
    # (every exported ordinal from the NE entry table, much broader).
    # Dedup by (module, true_bytes_hex) so we don't process the same function
    # twice when both sources point at it.
    seen = set()
    all_candidates = []
    redisasm_count = 0
    skipped = 0
    src_counts = {'pass23': 0, 'pass25': 0}

    def _load_dir(src_dir, source_name):
        nonlocal redisasm_count, skipped
        if not src_dir.exists():
            return
        for jp in sorted(src_dir.glob('*.json')):
            d = json.loads(jp.read_text(encoding='utf-8'))
            for c in d['candidates']:
                c['module'] = d.get('module', jp.stem)
                true_hex = c.get('true_bytes_hex', '')
                if not true_hex:
                    skipped += 1
                    continue
                key = (c['module'], true_hex)
                if key in seen:
                    continue
                seen.add(key)
                seg_bytes = bytes.fromhex(true_hex)
                new_lines = []
                for ins in md_global.disasm(seg_bytes, 0):
                    new_lines.append(
                        f"        {ins.mnemonic}  {ins.op_str}".rstrip())
                if not new_lines:
                    skipped += 1
                    continue
                c['asm_lines'] = new_lines
                c['body_bytes_hex'] = true_hex
                c.setdefault('source', source_name)
                redisasm_count += 1
                src_counts[source_name] += 1
                all_candidates.append(c)

    # Load every available candidate source. The (module, true_bytes_hex)
    # dedup `seen` set means we won't process the same function twice
    # even when several passes flag it.
    src_counts['pass27'] = 0
    _load_dir(PASS27, 'pass27')   # superset (exports + internals), if run
    _load_dir(PASS25, 'pass25')   # exports only (always-present baseline)
    _load_dir(PASS23, 'pass23')   # legacy mini-stubs

    print(f'Loaded {len(all_candidates)} candidates '
          f'(pass23={src_counts["pass23"]}, pass25={src_counts["pass25"]}, '
          f'pass27={src_counts["pass27"]}, '
          f'{redisasm_count} re-disassembled, {skipped} skipped).\n')

    # Stage MASM toolchain
    combined = REPO / 'tools' / 'dos' / 'combined'
    for tool in ['MASM.EXE']:
        dest = WORK / tool
        if not dest.exists():
            shutil.copy2(combined / tool, dest)

    # Generate initial .asm files
    asm_files = stage_asm_files(all_candidates)

    history = []  # per-round results

    import time
    for round_idx in range(MAX_REFINEMENT_ROUNDS):
        print(f'\n=== Round {round_idx + 1} ===')
        t0 = time.time()
        run_masm_parallel(asm_files)
        elapsed = time.time() - t0
        results = inspect_round(asm_files)
        matched = sum(1 for x in results if x['match'])
        rate = len(results) / elapsed if elapsed > 0 else 0
        print(f'  {matched}/{len(results)} byte-exact matches '
              f'(took {elapsed:.1f}s, {rate:.1f} func/s)')
        history.append({'round': round_idx + 1, 'matched': matched,
                         'total': len(results), 'seconds': elapsed})
        if matched == len(results):
            print('  ALL candidates matched - stopping early.')
            break
        if round_idx + 1 < MAX_REFINEMENT_ROUNDS:
            refined = refine_candidates(asm_files, results)
            print(f'  Refined {len(refined)} candidates for next round.')
            if not refined:
                print('  No further refinement possible - stopping.')
                break

    # Save final results
    final_results = inspect_round(asm_files)
    (PASS24 / 'results.json').write_text(json.dumps(final_results, indent=2),
                                          encoding='utf-8')
    (PASS24 / 'history.json').write_text(json.dumps(history, indent=2),
                                          encoding='utf-8')

    # Print final summary
    matched = sum(1 for r in final_results if r['match'])
    total = len(final_results)
    pct = 100 * matched / total if total else 0
    print(f'\n===== FINAL RESULTS =====')
    print(f'  {matched}/{total} byte-exact matches ({pct:.1f}%)')
    print(f'  Refinement history: {history}')
    print()
    # Per-module summary (instead of per-function, which is overwhelming
    # when there are thousands of candidates).
    from collections import defaultdict
    mod_stats = defaultdict(lambda: [0, 0])  # module -> [matched, total]
    for r in final_results:
        mod_stats[r['module']][1] += 1
        if r['match']:
            mod_stats[r['module']][0] += 1
    print(f"{'Module':<14}{'Matched':>9}{'Total':>9}{'Pct':>7}")
    print('-' * 40)
    for m, (mt, t) in sorted(mod_stats.items()):
        p = 100 * mt / t if t else 0
        print(f'{m:<14}{mt:>9}{t:>9}{p:>6.1f}%')

    # Markdown report
    md = ['# Pass 24: Byte-exact verification batch\n']
    md.append(f'Verified **{matched}/{len(final_results)}** mini-stub '
              f'functions byte-exact against original binaries using MASM '
              f'4.0 + DOSBox-X.\n')
    md.append('Refinement rounds: ' + ' → '.join(
        f"{h['matched']}/{h['total']}" for h in history) + '\n')
    md.append('| Module | Function | Match | Bytes | Note |')
    md.append('|---|---|---|---|---|')
    for r in final_results:
        ok = 'YES' if r['match'] else 'no'
        md.append(f"| `{r['module']}` | `{r['name']}` | {ok} | "
                  f"`{r['expected_hex']}` | {r['note']} |")
    (PASS24 / 'summary.md').write_text('\n'.join(md), encoding='utf-8')


if __name__ == '__main__':
    main()
