def stage_asm_files(candidates):
    """Generate the per-candidate .ASM files. Returns a list of dicts with
    metadata ready for batch assembly. Filenames are kept to 8 chars max
    (MASM 4.0 8.3 limitation)."""
    out = []
    short_used = set()
    for c in candidates:
        unique = f"{c['module']}_{c['name']}"
        short = unique[:8].upper()
        base = short
        n = 0
        while base in short_used:
            n += 1
            # When N is 1..9 we have 7+1=8 chars; for n>=10 we have to drop
            # another char of the prefix so the total stays at 8.
            digits = str(n)
            base = (short[:8 - len(digits)] + digits)
        short_used.add(base)
        asm_path = WORK / f'{base}.ASM'
        byte_seq = bytes.fromhex(c['body_bytes_hex'])
        src = make_asm_source(c['name'], c['asm_lines'], byte_seq,
                                has_frame=c.get('has_frame', False))
        asm_path.write_text(src, encoding='ascii')
        out.append({**c, 'short': base, 'asm_path': asm_path,
                    'byte_seq': byte_seq})
    return out


def asm_hash(asm_path):
    """SHA256 of the .ASM source. Used as cache key for the resulting OBJ/LST."""
    return hashlib.sha256(asm_path.read_bytes()).hexdigest()[:16]


def _worker_run_masm(worker_dir_str, shorts):
    """Run MASM 4.0 on the given list of shortnames inside `worker_dir`.
    Returns (worker_dir, shorts) on completion. Used as the unit of work for
    the ProcessPoolExecutor pool.

    The worker dir already contains MASM.EXE plus a build.bat that processes
    exactly the shortnames in `shorts`. This function only invokes DOSBox-X.
    """
    worker_dir = Path(worker_dir_str)
    dosbuild = REPO / 'tools' / 'dos' / 'dosbuild.sh'
    cmd = ['wsl', '--', 'bash',
           str(dosbuild).replace('\\', '/').replace('C:', '/mnt/c'),
           str(worker_dir).replace('\\', '/').replace('C:', '/mnt/c'),
           'build.bat']
    subprocess.run(cmd, capture_output=True, text=True, timeout=900)
    return worker_dir_str, shorts


def run_masm_parallel(asm_files, n_workers=DEFAULT_WORKERS):
    """Compile every asm in `asm_files` via N parallel DOSBox-X workers,
    using a SHA256-of-source cache to skip already-built sources.

    Returns dict short -> {'obj_path', 'lst_path', 'cached': bool}.
    """
    CACHE.mkdir(parents=True, exist_ok=True)

    # Step 1: separate cached vs needs-build
    to_build = []           # list of asm_files entries
    cached_results = {}     # short -> {obj_path, lst_path, cached: True}
    for a in asm_files:
        h = asm_hash(a['asm_path'])
        a['hash'] = h
        cached_obj = CACHE / f'{h}.obj'
        cached_lst = CACHE / f'{h}.lst'
        if cached_obj.exists() and cached_lst.exists():
            # Copy out to worker view of WORK so refine/inspect can find them
            target_obj = WORK / f"{a['short']}.OBJ"
            target_lst = WORK / f"{a['short']}.LST"
            shutil.copy2(cached_obj, target_obj)
            shutil.copy2(cached_lst, target_lst)
            cached_results[a['short']] = {
                'obj_path': target_obj, 'lst_path': target_lst,
                'cached': True}
        else:
            to_build.append(a)

    print(f'  Cache: {len(cached_results)} hits / '
          f'{len(to_build)} need building '
          f'(workers={n_workers})')
    if not to_build:
        return cached_results

    # Step 2: chunk the to_build list across N worker subdirs, each with
    # its own MASM.EXE and its own build.bat.
    n_workers = max(1, min(n_workers, len(to_build)))
    chunks = [[] for _ in range(n_workers)]
    for i, a in enumerate(to_build):
        chunks[i % n_workers].append(a)

    combined = REPO / 'tools' / 'dos' / 'combined'
    worker_jobs = []
    for wid, chunk in enumerate(chunks):
        if not chunk:
            continue
        wdir = WORK / f'worker_{wid}'
        wdir.mkdir(parents=True, exist_ok=True)
        # Stage MASM.EXE (cheap copy)
        if not (wdir / 'MASM.EXE').exists():
            shutil.copy2(combined / 'MASM.EXE', wdir / 'MASM.EXE')
        # Copy each .ASM into the worker dir and write its build.bat
        bat = ['echo === worker batch === > out.txt']
        for a in chunk:
            tgt = wdir / f"{a['short']}.ASM"
            shutil.copy2(a['asm_path'], tgt)
            bat.append(f"masm {a['short']},{a['short']},{a['short']},nul; "
                       f"> {a['short']}.LOG")
        bat.append('echo === DONE === >> out.txt')
        (wdir / 'build.bat').write_text('\n'.join(bat) + '\n',
                                         encoding='ascii')
        worker_jobs.append((str(wdir), [a['short'] for a in chunk]))

    # Step 3: run all workers in parallel
    with ProcessPoolExecutor(max_workers=n_workers) as pool:
        futures = [pool.submit(_worker_run_masm, wd, shorts)
                    for wd, shorts in worker_jobs]
        for fut in as_completed(futures):
            fut.result()  # propagate exceptions

    # Step 4: collect produced OBJ/LST back to the main WORK dir AND save
    # them to the cache for future runs.
    results = dict(cached_results)
    for a in to_build:
        wdir = None
        # Find which worker handled this one
        for wd_str, shorts in worker_jobs:
            if a['short'] in shorts:
                wdir = Path(wd_str)
                break
        if wdir is None:
            continue
        src_obj = wdir / f"{a['short']}.OBJ"
        src_lst = wdir / f"{a['short']}.LST"
        tgt_obj = WORK / f"{a['short']}.OBJ"
        tgt_lst = WORK / f"{a['short']}.LST"
        # Move into WORK
        if src_obj.exists():
            shutil.copy2(src_obj, tgt_obj)
            # Stash into cache
            shutil.copy2(src_obj, CACHE / f"{a['hash']}.obj")
        if src_lst.exists():
            shutil.copy2(src_lst, tgt_lst)
            shutil.copy2(src_lst, CACHE / f"{a['hash']}.lst")
        results[a['short']] = {
            'obj_path': tgt_obj, 'lst_path': tgt_lst, 'cached': False}
    return results


def _lst_byte_stream(lst_path, func_name):
    """Concatenate the bytes MASM emitted for the named PROC, in offset
    order. Returns None if the .LST is missing/unparseable."""
    if not lst_path.exists():
        return None
    records = parse_lst_instructions(lst_path, func_name)
    if not records:
        return None
    out = bytearray()
    for r in sorted(records, key=lambda x: x['offset']):
        # Records can be sparse (offset jumps for ALIGN, etc.). Pad with
        # zeros so absolute offsets remain meaningful.
        while len(out) < r['offset']:
            out.append(0)
        out.extend(r['bytes'])
    return bytes(out)


def inspect_round(asm_files):
    """For each file, check whether MASM produced bytes byte-exact to the
    function's expected bytes.

    Two checks are performed in order:

    1. The .LST (authoritative): we sum every instruction's bytes from the
       MASM listing for our PROC and compare to the expected sequence. If
       they match, MASM emitted byte-exact code regardless of any OBJ
       FIXUP records that might have rewritten relocation slots later.

    2. The .OBJ (fallback): if for some reason we can't reconstruct the
       byte stream from the .LST (e.g. MASM aborted with a syntax error
       and produced no records), we fall back to scanning the .OBJ for
       the expected bytes - which is brittle in the presence of
       relocations but is still useful for short, FIXUP-free functions.
    """
    results = []
    for a in asm_files:
        obj = WORK / f"{a['short']}.OBJ"
        lst = WORK / f"{a['short']}.LST"
        expected = a['byte_seq']

        # 1. LST-based check
        lst_bytes = _lst_byte_stream(lst, a['name'])
        if lst_bytes is not None and lst_bytes.startswith(expected):
            actual = expected
            match = True
            note = f'MASM .LST byte-exact ({len(expected)} bytes)'
        else:
            # 2. OBJ-based fallback
            actual, note = find_func_bytes_in_obj(obj, expected)
            match = (actual == expected)
            if not match and lst_bytes is not None:
                # Compare LST output to expected for a more precise note.
                common = 0
                while (common < min(len(lst_bytes), len(expected))
                        and lst_bytes[common] == expected[common]):
                    common += 1
                if common == len(expected):
                    actual = expected
                    match = True
                    note = f'MASM .LST byte-exact ({common} bytes)'
                else:
                    note = (f"LST diverges at byte {common}: "
                             f"exp[{expected[common]:02X}] vs "
                             f"act[{lst_bytes[common]:02X}]")

        results.append({
            **{k: a[k] for k in ('module', 'name', 'short',
                                   'instruction_count')},
            'expected_hex': expected.hex().upper(),
            'actual_hex': (actual.hex().upper() if actual is not None
                            else (lst_bytes.hex().upper() if lst_bytes
                                  else '')),
            'match': match,
            'note': note,
            'lst_path': str(lst),
            'asm_path': str(a['asm_path']),
        })
    return results


def refine_candidates(asm_files, results):
    """For every non-matching candidate that produced an OBJ + LST, REBUILD
    its .asm body from the .LST itself - keeping each instruction's source
    text when it assembled correctly, and replacing it with a `db` directive
    when MASM picked a different encoding. Returns the list of files that
    were actually refined."""
    refined = []
    for a, r in zip(asm_files, results):
        if r['match']:
            continue
        lst = Path(r['lst_path'])
        if not lst.exists():
            continue
        records = parse_lst_instructions(lst, a['name'])
        if not records:
            continue
        new_body, n_changed = rebuild_body_from_lst(records, a['byte_seq'])
        if new_body is None or n_changed == 0:
            continue
        # Rewrite the .asm with the new body
        current = a['asm_path'].read_text(encoding='ascii').splitlines()
        proc_start, proc_end = None, None
        for i, ln in enumerate(current):
            if re.search(rf"\b{re.escape(a['name'])}\s+PROC\s+FAR", ln):
                proc_start = i + 1
            elif proc_start is not None and re.search(
                    rf"\b{re.escape(a['name'])}\s+ENDP", ln):
                proc_end = i
                break
        if proc_start is None or proc_end is None:
            continue
        new_full = current[:proc_start] + new_body + current[proc_end:]
        a['asm_path'].write_text('\n'.join(new_full) + '\n', encoding='ascii')
        refined.append((a['short'], n_changed))
    return refined


